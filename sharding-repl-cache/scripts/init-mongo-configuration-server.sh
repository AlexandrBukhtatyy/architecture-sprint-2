#!/bin/bash
############################################################
# Help                                                     #
############################################################
Help()
{
   # Display Help
   echo "Первичная настройка сервера конфигурации"
}
############################################################
############################################################
# Arguments checking                                       #
############################################################
############################################################
if [ "$1" = "" ]
then
  echo "Service name is requered!"
  exit
fi

if [ "$2" = "" ]
then
  echo "Port is requered!"
  exit
fi
############################################################
############################################################
# Main program                                             #
############################################################
############################################################

docker compose exec -T $1 mongosh --port $2 <<EOF
rs.initiate(
  {
    _id : "config_server",
       configsvr: true,
    members: [
      { _id : 0, host : "$1:$2" }
    ]
  }
);
exit();
EOF